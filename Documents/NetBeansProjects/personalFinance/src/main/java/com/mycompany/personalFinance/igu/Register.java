    private String isEmail(String email) {
        String email_reg = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        Pattern pattern = Pattern.compile(email_reg);
        Matcher matcher = pattern.matcher(email);
        List<User> userList = control.getUsers();
        if (matcher.matches() == true) {
            for (User user : userList) {
                if(user.getEmail().equals(email)){
                    return null;
                } 
            }
            return email;
        }
        return null;
    }

    private boolean validatePassword(String password) {
        if (password.length() < 8 || !containsLetterAndNumber(password)) {
            return false;
        } else {
            return true;
        }
    }

    private boolean containsLetterAndNumber(String str) {
        return Pattern.compile("[a-zA-Z]").matcher(str).find() && Pattern.compile("[0-9]").matcher(str).find();
    }
